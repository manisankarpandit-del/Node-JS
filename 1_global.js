//...dirname

console.log(__dirname); //it will give the current directory path where the file is located. It is a global variable in Node.js that represents the directory name of the current module.

  
//module    //it will print module object
console.log(module); //it will give the details of the current module, including its id, filename, loaded status, and exports. It is a global variable in Node.js that represents the current module being executed.

//__filename  //it will give the current file name with path. It is a global variable in Node.js that represents the filename of the current module being executed.
console.log(__filename); //it will print the current file name


//process  //it will print process object
console.log(process); //it will give the details of the current process, including its id, version, platform, and environment variables. It is a global variable in Node.js that provides information about the current process and allows you to interact with it.


//require  //it will print require function
console.log(require); //it will give the details of the require function, which is used to import modules in Node.js. It is a global variable in Node.js that allows you to include and use modules in your code.

